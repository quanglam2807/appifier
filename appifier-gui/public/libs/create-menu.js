const {
  app,
  Menu,
  shell,
} = require('electron');

const sendMessageToWindow = require('./send-message-to-window');

const createMenu = () => {
  const template = [
    {
      label: 'Edit',
      submenu: [
        { role: 'undo' },
        { role: 'redo' },
        { type: 'separator' },
        { role: 'cut' },
        { role: 'copy' },
        { role: 'paste' },
        { role: 'pasteandmatchstyle' },
        { role: 'delete' },
        { role: 'selectall' },
      ],
    },
    {
      label: 'View',
      submenu: [
        { role: 'reload' },
        {
          label: 'Debug...',
          role: 'toggledevtools',
        },
        { type: 'separator' },
        { role: 'togglefullscreen' },
      ],
    },
    {
      role: 'window',
      submenu: [
        { role: 'minimize' },
        { role: 'close' },
      ],
    },
    {
      role: 'help',
      submenu: [
        {
          label: 'Learn More',
          click: () => shell.openExternal('https://webcatalog.io'),
        },
      ],
    },
  ];

  if (process.platform !== 'darwin') {
    template[template.length - 1].submenu.push({
      label: 'About WebCatalog',
      click: () => sendMessageToWindow('open-about-dialog'),
    });
  }

  if (process.platform === 'linux') {
    template[0].submenu.push({ role: 'separator' });
    template[0].submenu.push({
      label: 'Preferences...',
      accelerator: 'Ctrl+P',
      click: () => sendMessageToWindow('open-preferences-dialog'),
    });
  }


  if (process.platform === 'darwin') {
    template.unshift({
      label: app.getName(),
      submenu: [
        {
          label: 'About WebCatalog',
          click: () => sendMessageToWindow('open-about-dialog'),
        },
        { type: 'separator' },
        {
          label: 'Preferences...',
          accelerator: 'Cmd+,',
          click: () => sendMessageToWindow('open-preferences-dialog'),
        },
        { type: 'separator' },
        { role: 'services', submenu: [] },
        { type: 'separator' },
        { role: 'hide' },
        { role: 'hideothers' },
        { role: 'unhide' },
        { type: 'separator' },
        { role: 'quit' },
      ],
    });

    // Window menu
    template[3].submenu = [
      { role: 'close' },
      { role: 'minimize' },
      { role: 'zoom' },
      { type: 'separator' },
      { role: 'front' },
    ];
  }

  const menu = Menu.buildFromTemplate(template);
  Menu.setApplicationMenu(menu);
};

module.exports = createMenu;
